
// Q5: Difference between int, Integer, and String
public class Q5_IntVsIntegerVsString {
    public static void main(String[] args) {
        int x = 10; // primitive type
        Integer y = 20; // wrapper class (object)
        String z = "30"; // text data

        System.out.println("int: " + x);
        System.out.println("Integer: " + y);
        System.out.println("String: " + z);
    }
}
